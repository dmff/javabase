package offer.vo;

public class TreeNode {

	public int val;
	public TreeNode left;
	public TreeNode right;

	
	
	public TreeNode(int val) {
		super();
		this.val = val;
	}

	@Override
	public String toString() {
		return "TreeNode [val=" + val + ", left=" + left + ", right=" + right + "]";
	}

	
}
